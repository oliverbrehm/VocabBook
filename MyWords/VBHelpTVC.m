@interface VBHelpTVC ()

@property (strong, nonatomic) NSArray* helpPageNames;

@end

@implementation VBHelpTVC

- (id)initWithStyle:(UITableViewStyle)style
{
    self = [super initWithStyle:style];
    if (self) {
        // Custom initialization
    }
    return self;
}

- (void)viewDidLoad
{
    [super viewDidLoad];
    
    self.helpPageNames = @[@"Getting started", @"Quiz algorithm", @"Test"];
}

- (void)didReceiveMemoryWarning
{
    [super didReceiveMemoryWarning];
    // Dispose of any resources that can be recreated.
}

#pragma mark - Table view data source

- (NSInteger)numberOfSectionsInTableView:(UITableView *)tableView
{
    return 1;
}

- (NSInteger)tableView:(UITableView *)tableView numberOfRowsInSection:(NSInteger)section
{

    return [self.helpPageNames count];
}

- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath
{
    UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:@"HelpPageCell" forIndexPath:indexPath];
    
    NSString *key = [NSString stringWithFormat:@"%@PageTitle", self.helpPageNames[indexPath.row]];
    cell.textLabel.text = NSLocalizedString(key, @"PageTitle");
    
    return cell;
}

